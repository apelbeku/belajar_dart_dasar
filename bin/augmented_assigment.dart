void main(){

  /// Augmented Assigment
  /**
   * a = a + 10;
   * a += 10;
   * a -= 10;
   * a *= 10;
   * a /= 10;
   * a ~/= 10;
   * a %= 10;
   *  */
  var a = 10;
  a += 10;

  print(a);

  /// Increment & Decrement
  var i = 0;
  var j = i++; // j = i; i++
  var k = ++i; // k = k++; i

  print(j);
  print(k);
}